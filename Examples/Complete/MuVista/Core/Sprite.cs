        public static Mesh createSprite(float2 size)
        {
            float3[] verts = new float3[8];
            float3[] norms = new float3[8];
            ushort[] tris = new ushort[12];

            verts[0] = new float3 { x = +0.5f * size.x, y = -0.5f * size.y, z = 0 };
            verts[1] = new float3 { x = +0.5f * size.x, y = +0.5f * size.y, z = 0 };
            verts[2] = new float3 { x = -0.5f * size.x, y = +0.5f * size.y, z = 0 };
            verts[3] = new float3 { x = -0.5f * size.x, y = -0.5f * size.y, z = 0 };

            for (int i = 0; i < 8; i++)
            {
                if (i < 4)
                    norms[i] = -float3.UnitZ;
            }

            tris[0] = 1;
            tris[1] = 2;
            tris[2] = 0;
            tris[3] = 2;
            tris[4] = 3;
            tris[5] = 0;

            return new Mesh { Vertices = verts, Normals = norms, Triangles = tris };
        }


